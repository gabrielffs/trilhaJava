// Conversão de Temperatura: Escreva um programa para converter valores de temperatura entre Celsius
// e Fahrenheit usando operações aritméticas e entrada do usuário.

public class Ex1_2 {
  public static void main(String[] args) {
    // Declaração de variáveis
    double c = 23.0;
    double f = 0;

    // Cálculo
    f = (c * 1.8) + 32;

    // Exibição
    System.out.println(f);
  }
}
